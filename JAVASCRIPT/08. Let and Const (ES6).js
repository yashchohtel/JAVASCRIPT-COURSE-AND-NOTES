/*

LET AND CONST :- 

1. let :- The let keyword is used to declare variable with block scope variables declared with let are mutable, meaning their values can be reassigned.

2. const :- The const keyword is used to declare variable with block scope, but once a value is assigned to a const variable, it cannot be reassigned. const variable are immutable.






















*/ 

